package group.demo.Entity;

import java.util.List;

public class MiObjeto {
    private int comun;
    private int lo;
    private int  ca;
    private List<Integer> p;
    private List<Integer> n;

    // Getters y Setters


    public int getComun() {
        return comun;
    }

    public void setComun(int comun) {
        this.comun = comun;
    }

    public int getLo() {
        return lo;
    }

    public void setLo(int lo) {
        this.lo = lo;
    }

    public int getCa() {
        return ca;
    }

    public void setCa(int ca) {
        this.ca = ca;
    }

    public List<Integer> getP() {
        return p;
    }

    public void setP(List<Integer> p) {
        this.p = p;
    }

    public List<Integer> getN() {
        return n;
    }

    public void setN(List<Integer> n) {
        this.n = n;
    }
}
